#!/usr/bin/env python3
"""Render the generated core Make targets doc from `make help` output."""

from __future__ import annotations

import argparse
import html
import os
from pathlib import Path
import re
import shutil
import subprocess
import sys
import tempfile


REPO_ROOT = Path(__file__).resolve().parents[3]
DEFAULT_OUTPUT = REPO_ROOT / "docs/reference/generated/core_targets.generated.md"
TARGET_LINE_RE = re.compile(r"^(\S+)\s{2,}(.+)$")
DEFAULT_MODULE_ENV = {
    "OBSERVABILITY_ENABLED": "false",
    "WORKFLOWS_ENABLED": "false",
    "LANGFUSE_ENABLED": "false",
    "POSTGRES_ENABLED": "false",
    "NEO4J_ENABLED": "false",
    "OBJECT_STORAGE_ENABLED": "false",
    "RABBITMQ_ENABLED": "false",
    "DNS_ENABLED": "false",
    "PUBLIC_ENDPOINTS_ENABLED": "false",
    "SECRETS_MANAGER_ENABLED": "false",
    "KMS_ENABLED": "false",
    "IDENTITY_AWARE_PROXY_ENABLED": "false",
}
TEMP_REPO_PATHS = ("Makefile", "make", "scripts")


def _copy_tree(src: Path, dst: Path) -> None:
    if src.is_dir():
        shutil.copytree(src, dst)
        return
    dst.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src, dst)


def _default_target_env() -> dict[str, str]:
    env = os.environ.copy()
    env.update(DEFAULT_MODULE_ENV)
    return env


def _prepare_temp_repo(repo_root: Path, temp_root: Path) -> None:
    for relative in TEMP_REPO_PATHS:
        _copy_tree(repo_root / relative, temp_root / relative)


def _load_targets() -> list[tuple[str, str]]:
    # Materialized optional-module targets depend on the caller environment.
    # Build the reference doc from a temp copy rendered with the contract-default
    # module flags so the tracked doc stays stable and check-mode is non-mutating.
    env = _default_target_env()
    with tempfile.TemporaryDirectory() as tmpdir:
        temp_repo = Path(tmpdir) / "repo"
        _prepare_temp_repo(REPO_ROOT, temp_repo)

        render = subprocess.run(
            ["scripts/bin/blueprint/render_makefile.sh"],
            cwd=temp_repo,
            env=env,
            check=False,
            capture_output=True,
            text=True,
        )
        if render.returncode != 0:
            message = render.stderr.strip() or render.stdout.strip()
            raise RuntimeError(f"failed to render default Make surface: {message}")

        completed = subprocess.run(
            ["make", "help"],
            cwd=temp_repo,
            env=env,
            check=False,
            capture_output=True,
            text=True,
        )
    if completed.returncode != 0:
        message = completed.stderr.strip() or completed.stdout.strip()
        raise RuntimeError(f"failed to run `make help`: {message}")

    targets: list[tuple[str, str]] = []
    for raw_line in completed.stdout.splitlines():
        line = raw_line.rstrip()
        match = TARGET_LINE_RE.match(line)
        if match is None:
            continue
        targets.append((match.group(1).strip(), match.group(2).strip()))
    if not targets:
        raise RuntimeError("no Make targets discovered from `make help`")
    return targets


def _escape_markdown_table_cell(value: str) -> str:
    """Escape table-cell content so generated markdown is MDX-safe."""
    escaped = html.escape(value, quote=False)
    return escaped.replace("|", r"\|")


def _render_markdown(targets: list[tuple[str, str]]) -> str:
    header = (
        "<!-- Generated by scripts/bin/quality/render_core_targets_doc.py. -->\n"
        "<!-- Source of truth: `make help` output. -->\n\n"
        "# Core Make Targets\n\n"
        "This file is auto-generated. Do not edit it manually.\n\n"
        "| Target | Description |\n"
        "| --- | --- |\n"
    )
    rows = [
        f"| `{_escape_markdown_table_cell(name)}` | {_escape_markdown_table_cell(description)} |"
        for name, description in targets
    ]
    return header + "\n".join(rows) + "\n"


def _write_if_needed(output_path: Path, content: str) -> bool:
    if output_path.exists() and output_path.read_text(encoding="utf-8") == content:
        return False
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(content, encoding="utf-8")
    return True


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--output",
        type=Path,
        default=DEFAULT_OUTPUT,
        help="Path to generated markdown output.",
    )
    parser.add_argument(
        "--check",
        action="store_true",
        help="Exit non-zero when output file is out of date.",
    )
    args = parser.parse_args()

    try:
        targets = _load_targets()
    except RuntimeError as exc:
        print(str(exc), file=sys.stderr)
        return 1

    rendered = _render_markdown(targets)
    output = args.output.resolve()

    if args.check:
        if not output.exists():
            print(f"core targets doc missing: {output}", file=sys.stderr)
            return 1
        current = output.read_text(encoding="utf-8")
        if current != rendered:
            print(
                f"core targets doc is out of date: {output}\n"
                "Run: python3 scripts/bin/quality/render_core_targets_doc.py",
                file=sys.stderr,
            )
            return 1
        return 0

    changed = _write_if_needed(output, rendered)
    if changed:
        print(f"rendered core targets doc: {output}")
    else:
        print(f"core targets doc already up to date: {output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
